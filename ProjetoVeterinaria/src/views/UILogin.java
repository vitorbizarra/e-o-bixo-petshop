    public void BuscarUsuario(Veterinario veterinario) {
        // Dados para comparaÃ§Ã£o
        veterinario.setEmail(txtLogin.getText());
        veterinario.setSenha(txtPass.getText());
        String senhaBanco;

        // Conecta ao banco
        this.conectar.conectaBanco();
        try {
            //Realiza consulta no banco
            this.conectar.executarSQL(
                    "SELECT `Senha` "
                    + "FROM `veterinario` "
                    + "WHERE "
                    + "Email = "
                    + "'" + veterinario.getEmail() + "';");

            // Loop para obter o resultado
            while (this.conectar.getResultSet().next()) {
                senhaBanco = this.conectar.getResultSet().getString("Senha");

                // Confere se as senhas do usuÃ¡rio informado conferem
                if (senhaBanco.equals(veterinario.getSenha())) {
                    System.out.println("Logou");
                    // Renderiza views
                    uiSistema.setVisible(true);
                    this.setVisible(false);
                } else {
                    System.out.println("Login ou senha incorretos.");
                    JOptionPane.showMessageDialog(null, "Login ou senha incorretos.");
                }
            }
        } catch (Exception e) {
            System.out.println("Erro ao consultar usuÃ¡rio: " + e.getMessage());
            JOptionPane.showMessageDialog(null, "Erro ao consultar o usuÃ¡rio.");
        } finally {
            this.conectar.fechaBanco();
        }
    }

